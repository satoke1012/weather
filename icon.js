/*
==========================================
 アイコン取得
==========================================
*/

function getWeatherIcon(code){

    code = String(code);

    if(WEATHER_ICONS[code]){

        return WEATHER_ICONS[code];

    }

    return "❔";

}



/*
==========================================
 天気文字
==========================================
*/

function getWeatherText(code){

    code = String(code);

    switch(code){

        case "100":
            return "晴れ";

        case "101":
            return "晴れ時々くもり";

        case "102":
            return "晴れ一時雨";

        case "103":
            return "晴れ時々雨";

        case "200":
            return "くもり";

        case "201":
            return "くもり時々晴れ";

        case "202":
            return "くもり一時雨";

        case "300":
            return "雨";

        case "301":
            return "雨時々晴れ";

        case "302":
            return "雨時々くもり";

        case "303":
            return "雨のち雪";

        case "400":
            return "雪";

        case "401":
            return "雪時々晴れ";

        case "402":
            return "雪時々くもり";

        default:
            return "天気情報";
    }

}
